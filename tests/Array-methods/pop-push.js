//push() method is used to add a new value to last index of an array
//we cant store this in another variable and can't create new array it will modifiy this array only
var num = [10,20,30,40,50];
console.log(num);

//PUSH() METHOD
num.push(100); 

console.log(num);

//this will delete the last value in the array, only last value
//POP() METHOD
num.pop();

console.log(num);